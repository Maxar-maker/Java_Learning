import java.util.*;
public class Pattern_Program
{
    public static void main()
    {
        for(int i=1;i<=9;i+=2)
        {
            for(int j=i;j>=1;j-=2)
            {
                if(j==1)
                {
                    System.out.print(j);
                    
                }
                else
                {
                    System.out.print(j);
                }
            }
            System.out.println(" ");
        }
    }
}